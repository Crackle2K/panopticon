-- ── Atomic phase-transition functions ──────────────────────────────────────
-- These prevent race conditions when multiple clients fire the same trigger.

create or replace function try_start_voting(p_code text)
returns boolean
language plpgsql security definer
as $$
declare
  v_room_id uuid;
  v_not_ready int;
begin
  select id into v_room_id
    from rooms where code = p_code and status = 'playing';
  if v_room_id is null then return false; end if;

  select count(*) into v_not_ready
    from players where room_id = v_room_id and not has_seen_word;

  if v_not_ready = 0 then
    update rooms set status = 'voting'
      where id = v_room_id and status = 'playing';
    return found;
  end if;

  return false;
end;
$$;


create or replace function try_finish_game(p_code text)
returns boolean
language plpgsql security definer
as $$
declare
  v_room_id    uuid;
  v_not_voted  int;
  v_voted_out  text;
begin
  select id into v_room_id
    from rooms where code = p_code and status = 'voting';
  if v_room_id is null then return false; end if;

  select count(*) into v_not_voted
    from players where room_id = v_room_id and not has_voted;

  if v_not_voted = 0 then
    select vote into v_voted_out
      from player_secrets
      where player_id in (select id from players where room_id = v_room_id)
        and vote is not null
      group by vote
      order by count(*) desc
      limit 1;

    update rooms
      set status = 'finished', voted_out_id = v_voted_out
      where id = v_room_id and status = 'voting';
    return found;
  end if;

  return false;
end;
$$;

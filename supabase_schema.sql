-- ── Tables ─────────────────────────────────────────────────────────────────

create table if not exists rooms (
  id             uuid primary key default gen_random_uuid(),
  code           text unique not null,
  host_id        text not null,           -- player.id of the host (no FK to avoid circular dep)
  status         text not null default 'waiting',  -- waiting | playing | voting | finished
  word           text,                    -- revealed to everyone at end
  imposter_word  text,                    -- revealed to everyone at end
  imposter_id    text,                    -- revealed to everyone at end
  voted_out_id   text,                    -- player.id who got the most votes
  created_at     timestamptz default now()
);

create table if not exists players (
  id             text primary key,        -- UUID generated by the client (stored in localStorage)
  room_id        uuid references rooms(id) on delete cascade,
  name           text not null,
  is_host        boolean default false,
  has_seen_word  boolean default false,
  has_voted      boolean default false,
  joined_at      timestamptz default now(),
  unique (room_id, name)
);

-- Sensitive: never exposed to anon key — backend service key only
create table if not exists player_secrets (
  player_id   text primary key references players(id) on delete cascade,
  word        text not null,
  is_imposter boolean default false,
  vote        text                         -- player.id they voted for
);


-- ── Row-level security ──────────────────────────────────────────────────────

alter table rooms          enable row level security;
alter table players        enable row level security;
alter table player_secrets enable row level security;

-- rooms: anyone can read; only service role writes (via Flask)
create policy "rooms_select"  on rooms  for select using (true);

-- players: anyone can read; only service role writes
create policy "players_select" on players for select using (true);

-- player_secrets: NO access for anon — service role only bypasses RLS
-- (no policies needed; with RLS on and no policy, anon gets nothing)


